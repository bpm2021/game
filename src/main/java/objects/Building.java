package objects;

import java.awt.*;

public class Building {

    protected Integer width;
    protected Integer height;
    protected Integer x;
    protected Integer y;
    protected Color background;

    public Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }

    public Integer getWidth() {
        return width;
    }

    public Integer getHeight() {
        return height;
    }

    public Color getBackgroundColor() {
        return background;
    }
}
